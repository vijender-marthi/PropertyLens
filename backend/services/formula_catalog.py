    return {
        "metricKey": metric_key,
        "formulaDefinitionId": f"{metric_key}:{CATALOG_VERSION}",
        "pageKey": page_key,
        "sectionKey": section_key,
        "name": name,
        "shortDefinition": short_definition,
        "detailedDefinition": detailed_definition,
        "formula": "\n".join(lines),
        "formulaLines": lines,
        "inputDefinitions": list(inputs),
        "example": example,
        "sourcePriority": source_priority or [],
        "sourceType": source_type,
        "assumptions": assumptions or [],
        "exclusions": exclusions or [],
        "relatedMetricKeys": related or [],
        "displayFormat": display_format,
        "calculationFrequency": calculation_frequency,
        "version": CATALOG_VERSION,
        "searchTerms": search_terms or [],
    }



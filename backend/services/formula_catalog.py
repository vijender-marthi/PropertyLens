def _matches_query(formula: Dict[str, Any], query: str) -> bool:
    haystack = " ".join(
        str(part or "")
        for part in [
            formula.get("metricKey"),
            formula.get("name"),
            formula.get("shortDefinition"),
            formula.get("detailedDefinition"),
            formula.get("formula"),
            formula.get("pageKey"),
            formula.get("sectionKey"),
            " ".join(formula.get("searchTerms") or []),
            " ".join(formula.get("relatedMetricKeys") or []),
        ]
    ).lower()
    return query.lower() in haystack


def get_formula_catalog(
    *,
    page: Optional[str] = None,
    section: Optional[str] = None,
    source_type: Optional[str] = None,
    query: Optional[str] = None,
) -> Dict[str, Any]:
    formulas = FORMULA_DEFINITIONS
    if page:
        formulas = [item for item in formulas if item["pageKey"] == page]
    if section:
        formulas = [item for item in formulas if item["sectionKey"] == section]
    if source_type:
        formulas = [item for item in formulas if item["sourceType"] == source_type]
    if query:
        formulas = [item for item in formulas if _matches_query(item, query)]

    return {
        "version": CATALOG_VERSION,
        "pages": HELP_PAGES,
        "formulas": formulas,
        "audit": FORMULA_AUDIT_FINDINGS,
        "sourceTypes": ["reported", "calculated", "derived", "projected", "estimated", "manual", "mixed"],
    }
